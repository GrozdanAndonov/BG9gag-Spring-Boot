package com.example.s8.model;

public class Diuner {
	private String hlebche;
	private String sosche;
	private int gramaj;
	private int kartofki;
	private String meso;
	
	public Diuner() {

	}
	public String getHlebche() {
		return hlebche;
	}
	public void setHlebche(String hlebche) {
		this.hlebche = hlebche;
	}
	public String getSosche() {
		return sosche;
	}
	public void setSosche(String sosche) {
		this.sosche = sosche;
	}
	public int getGramaj() {
		return gramaj;
	}
	public void setGramaj(int gramaj) {
		this.gramaj = gramaj;
	}
	public int getKartofki() {
		return kartofki;
	}
	public void setKartofki(int kartofki) {
		this.kartofki = kartofki;
	}
	public String getMeso() {
		return meso;
	}
	public void setMeso(String meso) {
		this.meso = meso;
	}
	
	
}
